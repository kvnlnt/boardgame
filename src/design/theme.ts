interface GenerateCssProps {
  suffix?: string;
  psuedoSelector?: string;
}
export const GenerateCss = ({
  suffix = '',
  psuedoSelector = '',
}: GenerateCssProps) =>
  Object.entries(css)
    .map(([atom, declarations]) => {
      return `.${atom}${suffix}${psuedoSelector} { ${Object.entries(
        declarations
      )
        .map(
          ([prop, val]) =>
            `${prop.replace(/([A-Z])/g, '-$1').toLowerCase()}: ${val}; `
        )
        .join('')}}`;
    })
    .join('\n');

export const Css = `
@media (min-width: ${tokens.mobile}){
  ${GenerateCss({})}
  ${GenerateCss({ suffix: '_on_hover', psuedoSelector: ':hover' })}
}
@media (min-width: ${tokens.tablet}){
  ${GenerateCss({})}
  ${GenerateCss({ suffix: '_on_hover_on_tablet', psuedoSelector: ':hover' })}
}
@media (min-width: ${tokens.desktop}){
  ${GenerateCss({})}
  ${GenerateCss({ suffix: '_on_hover_on_desktop', psuedoSelector: ':hover' })}
}
`;

export const CreateStyleSheet = (css: string): void => {
  const style = document.createElement('style');
  style.innerHTML = css;
  document.getElementsByTagName('head')[0].appendChild(style);
};

type selector = keyof typeof css;
type selector_on_hover = `${selector}_on_hover`;
type selector_on_hover_tablet = `${selector}_on_hover_on_tablet`;
type selector_on_hover_desktop = `${selector}_on_hover_on_desktop`;
export type atoms =
  | ''
  | selector
  | selector_on_hover
  | selector_on_hover_tablet
  | selector_on_hover_desktop;
export const atomize = (...list: atoms[]) => {
  return list.join(' ');
};
